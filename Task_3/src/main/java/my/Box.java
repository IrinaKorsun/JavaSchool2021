package my;

public class Box {
    private int length;
    private int length1;
    private int length2;
    private int length3;
    private int length4;
    private int length5;

    public Box() {
    }

    public Box(int length, int length1, int length2, int length3, int length4, int length5) {
        this.length = length;
        this.length1 = length1;
        this.length2 = length2;
        this.length3 = length3;
        this.length4 = length4;
        this.length5 = length5;
    }

    public int getLength() {
        return length;
    }

    public int getLength1() {
        return length1;
    }

    public int getLength2() {
        return length2;
    }

    public int getLength3() {
        return length3;
    }

    public int getLength4() {
        return length4;
    }

    public int getLength5() {
        return length5;
    }
}
